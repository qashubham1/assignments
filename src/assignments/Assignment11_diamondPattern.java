package assignments;

public class Assignment11_diamondPattern {

	public static void main(String[] args) {
		
		for(int i=1; i <= 5 ; i++) {
			
			for(int j=1; j<=i; j++){
				
				System.out.print(j+ " ");
				
				
			}
				System.out.println();
			
			}
		for(int i=6; i>=9; i++) {// rows
			
			for(int j=1; j <= i; j--) {
				
				System.out.println(j + " ");
			}
			
		}System.out.println();
			
		}
		
		
		
		
	}

